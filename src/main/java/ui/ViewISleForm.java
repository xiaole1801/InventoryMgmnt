    private void printBtnActionPerformed(java.awt.event.ActionEvent evt) {                                         
      // TODO add your handling code here:
      String isleID = isleIDText.getText().toString();
      byte[] imageByte = Label.creatLabel(isleID.substring(0, 12), isleID, "Shipping In", trackingNoText.getText());
      Icon icon = new ImageIcon(imageByte);
      Object[] options = { "Yes", "No" };
      int response = JOptionPane.showOptionDialog(ViewISleForm.this, "Are you sure you want to print the tag?", "Warning", JOptionPane.YES_OPTION, JOptionPane.PLAIN_MESSAGE, icon, options, options[0]);
      if (response == 1) {
        return;
      }if (response == 0) {
        File printFile = Label.generateFinalImage("barcode-last.png",imageByte);
        if (printFile == null)
          return;
        PrintImage pi = new PrintImage();
        pi.print("barcode-last.png", 1);
      }
  } 
