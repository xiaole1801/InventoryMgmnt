    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
    	ISle isle = (ISle)HibernateUtil.findById(ISle.class, initIsle.getISleID());

    	isle.setCourierCompany(expressCoText.getText());
    	isle.setDeliveryPerson(deliveryPersonText.getText());
    	isle.setTrackingNum(trackingNoText.getText());
    	isle.setReceiptTime(receiptTimePicker.getDate());
    	isle.setStatus(statusComboBox.getSelectedItem().toString());
    	isle.setRemark(remarkTextArea.getText());
    	if (transferEngIDcombox.getSelectedIndex()>0)
    	{
    		isle.setTransferEngID(userlist.get(transferEngIDcombox.getSelectedIndex()-1).getUserID());
    		isle.setTransferTime(transferTimePicker.getDate());
    	}
    	
        HibernateUtil.update(isle);
        MainForm.gridTable.setModel(init.main.client.getISleUserInfoTM(0));
		JOptionPane.showMessageDialog(ViewISleForm.this, "The information of the ISle "+isleIDText.getText()+" is updated successfully!",
				"OK", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_addBtnActionPerformed

